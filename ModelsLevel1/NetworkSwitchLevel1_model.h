  void send_b_transport(unsigned port_index, tlm::tlm_generic_payload& trans, sc_core::sc_time& t) {
    if (port_index > get_port_count() || !is_master(port_index))
      return;

    if (port_index >= NodeMaster0_idx && port_index <= NodeMaster0_idx)
      return NodeMaster0->b_transport(trans, t);
    if (port_index >= NodeMaster1_idx && port_index <= NodeMaster1_idx)
      return NodeMaster1->b_transport(trans, t);
    if (port_index >= NodeMaster2_idx && port_index <= NodeMaster2_idx)
      return NodeMaster2->b_transport(trans, t);
    if (port_index >= NodeMaster3_idx && port_index <= NodeMaster3_idx)
      return NodeMaster3->b_transport(trans, t);
    if (port_index >= NodeMaster4_idx && port_index <= NodeMaster4_idx)
      return NodeMaster4->b_transport(trans, t);
    return;
    
  }

  unsigned send_transport_dbg(unsigned port_index, tlm::tlm_generic_payload& trans) {
    if (port_index > get_port_count() || !is_master(port_index))
      return 0;

    if (port_index >= NodeMaster0_idx && port_index <= NodeMaster0_idx)
      return NodeMaster0->transport_dbg(trans);
    if (port_index >= NodeMaster1_idx && port_index <= NodeMaster1_idx)
      return NodeMaster1->transport_dbg(trans);
    if (port_index >= NodeMaster2_idx && port_index <= NodeMaster2_idx)
      return NodeMaster2->transport_dbg(trans);
    if (port_index >= NodeMaster3_idx && port_index <= NodeMaster3_idx)
      return NodeMaster3->transport_dbg(trans);
    if (port_index >= NodeMaster4_idx && port_index <= NodeMaster4_idx)
      return NodeMaster4->transport_dbg(trans);
    
    return 0;
  }
  
  

